/**
 * Digraphs Data - Two letters that make one sound
 * Following synthetic phonics methodology
 */

export interface DigraphItem {
  id: string;
  digraph: string;
  sound: string;
  phonemeSpelling: string; // For speech synthesis
  description: string;
  position: "initial" | "final" | "both"; // Where digraph appears in words
  exampleWord: string;
  color: string;
}

export interface DigraphWord {
  id: string;
  word: string;
  digraphId: string;
  digraphPosition: "initial" | "final" | "medial";
  syllables: string[];
  phonemes: string[];
  difficulty: 1 | 2 | 3; // 1 = easy CVC+digraph, 2 = CCVC/CVCC, 3 = multisyllabic
  imageKeyword: string; // For image search
}

