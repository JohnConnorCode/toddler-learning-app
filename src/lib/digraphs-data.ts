// Helper functions
export function getDigraphById(id: string): DigraphItem | undefined {
  return DIGRAPHS.find((d) => d.id === id);
}

export function getWordsByDigraph(digraphId: string): DigraphWord[] {
  return DIGRAPH_WORDS.filter((w) => w.digraphId === digraphId);
}

export function getWordsByDifficulty(difficulty: 1 | 2 | 3): DigraphWord[] {
  return DIGRAPH_WORDS.filter((w) => w.difficulty === difficulty);
}

export function getRandomDigraphWords(count: number, digraphId?: string): DigraphWord[] {
  let words = digraphId ? getWordsByDigraph(digraphId) : [...DIGRAPH_WORDS];
  const shuffled = words.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getDigraphForWord(word: string): DigraphItem | undefined {
  const wordData = DIGRAPH_WORDS.find((w) => w.word === word);
  if (!wordData) return undefined;
  return getDigraphById(wordData.digraphId);
}
